"""
In Java: static Typing
    DataType var = Data

In Python: Dynamic Typing
    var = Data
"""

name = None # as null in Java

print(name)

name = 'James'

print(name)

print(type(name))  # type will return a type of the variable

age = 25

print( type(age))

gpa = 3.5

print( type(gpa) )

is_employed = True  #  boolean in upper case

print( type(is_employed))


s = '25'

#str(s)
s = int(s)  #  wrapping string to int using constructor
print(s * 5)



